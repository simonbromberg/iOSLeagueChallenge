import UIKit
import SwiftUI

extension UIColor {
  static let background = UIColor(named: "background")!
  static let border = UIColor(named: "border")!
  static let primaryText = UIColor(named: "primaryText")!
  static let secondaryText = UIColor(named: "secondaryText")!
  static let placeholderText = UIColor(named: "placeholderText")!
}

extension Color {
  static let background = Color(UIColor.background)
  static let border = Color(UIColor.border)
  static let primaryText = Color(UIColor.primaryText)
  static let secondaryText = Color(UIColor.secondaryText)
  static let placeholderText = Color(UIColor.placeholderText)
}
